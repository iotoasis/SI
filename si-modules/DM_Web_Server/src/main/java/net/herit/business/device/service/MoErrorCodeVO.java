package net.herit.business.device.service;

import java.io.Serializable;

import net.herit.common.util.StringUtil;


public class MoErrorCodeVO implements Serializable{

	/** ERROR_CODE */
	private String errorCode;
	/** ERROR_GRADE */
	private String errorGrade;
	/** ERROR_NAME */
	private String errorName;
	/** DESCRIPTION */
	private String description;


	public String getErrorCode() {
		return StringUtil.nvl(errorCode);
	}
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	public String getErrorName() {
		return StringUtil.nvl(errorName);
	}
	public void setErrorName(String errorName) {
		this.errorName = errorName;
	}
	public String getErrorGrade() {
		return StringUtil.nvl(errorGrade);
	}
	public void setErrorGrade(String errorGrade) {
		this.errorGrade = errorGrade;
	}

}
